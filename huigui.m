clc;clear all

%输入数据
x = [143 145 146 147 149 150 153 154 155 156 157 158 159 160 162 164]'; %身高
X = [ones(16,1),x];  %第一列是1 第二列是x
Y = [88 85 88 91 92 93 93 95 96 98 97 96 98 99 100 102]'; %腿长

%回归分析及检验
[b,bint,r,rint,stats] = regress(Y,X); %X后面还有α，α=0.05
% b,bint,stats  在命令窗口输入

%残差分析 残差作图
rcoplot(r,rint);

%预测及作图
z = b(1) + b(2)*2; % 2 可以是任何数
plot(x,Y,'k+',x,z,'r');